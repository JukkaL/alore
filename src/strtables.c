AWideChar AUpper2(AWideChar ch)
{
    if (ch < 255)
        return AUpperCase[ch];
    else {
        if (ch <= 9449) {
            int j;
            int masked;
            int first;
            int last;

            masked = ch & 63;

            first = AUpperLookupTable[ch >> 6];
            last = AUpperLookupTable[(ch >> 6) + 1];

            for (j = first;
                 j < last && AUpperTable[j].end < masked; j++);

            if (j < last && masked >= AUpperTable[j].beg
                && (AUpperTable[j].delta != -1
                    || ((masked - AUpperTable[j].beg) & 1) == 0))
                return ch + AUpperTable[j].delta;
            else
                return ch;
        } else if (ch >= 65345 && ch <= 65370)
            return ch - 32;
        else
            return ch;
    }
}


